#include <stdio.h>
#include <unistd.h>
#include <stdlib.h>

int		get_next_line(char **line);

int
	main(void)
{
	int		r;
	char	*line;

	line = NULL;
	while ((r = get_next_line(&line)) > 0)
	{
		printf("%s\n", line);
		free(line);
		line = NULL;
	}
	printf("%s", line);
	free(line);
	line = NULL;
}
